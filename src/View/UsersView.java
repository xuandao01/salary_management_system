    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int k = jTable1.getSelectedRow();
        User user = new User();
        user = listUser.get(k);
        hoten.setText(user.getHoTen());
        namsinh.setText(valueOf(user.getNamSinh()));
        quequan.setText(user.getQueQuan());
        luongcung.setText(valueOf(user.getLuongCung()));
        thuong.setText(valueOf(user.getThuong()));
        phat.setText(valueOf(user.getPhat()));
        username.setText(user.getUsername());
        for(int i=0;i<listAccount.size();i++){
            if(listAccount.get(i).getUsername().compareTo(user.getUsername()) == 0)
                password.setText(CaesarCipher.decrypt(listAccount.get(i).getPassword()));
        }
    }//GEN-LAST:event_jTable1MouseClicked

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(!validateUser()) return;
        else{
            for(int i=0;i<listAccount.size();i++){
                if(listAccount.get(i).getUsername().compareTo(username.getText()) == 0){
                    JOptionPane.showMessageDialog(rootPane, "Username đã tồn tại");
                    return;
                }
            }
            User user = new User();
            Account acc = new Account();
            user.setHoTen(hoten.getText());
            user.setNamSinh(Integer.parseInt(namsinh.getText()));
            user.setQueQuan(quequan.getText());
            user.setLuongCung(Float.parseFloat(luongcung.getText()));
            user.setThuong(Float.parseFloat(thuong.getText()));
            user.setPhat(Float.parseFloat(phat.getText()));
            user.setUsername(username.getText());
            acc.setUsername(username.getText());
            acc.setPassword(password.getText());
            AccountController.create(acc);
            UserController.createUser(user);
            JOptionPane.showMessageDialog(rootPane, "Thêm mới thành công!");
            this.setVisible(false);
            UsersView newView = new UsersView();
            newView.setVisible(true);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if(JOptionPane.showConfirmDialog(rootPane, "Chắc chắn xóa", "Delete User", 0) != 0) return;
        AccountController.delete(username.getText());
        UserController.deleteUser(username.getText());
        JOptionPane.showMessageDialog(rootPane, "Xóa thành công");
        updateTable();
        UsersView newView = new UsersView();
        newView.setVisible(true);
        this.setVisible(false);

    }//GEN-LAST:event_jButton3ActionPerformed

