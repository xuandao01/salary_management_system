    public void updateTable(){
        DefaultTableModel record = (DefaultTableModel) jTable1.getModel();
        record.setRowCount(0);
        listUser.removeAll(listUser);
        listUser = UserController.getAllUser();
        for(int i=0;i<listUser.size();i++){
            Vector colummData = new Vector();
            colummData.add(listUser.get(i).getHoTen());
            colummData.add(listUser.get(i).getNamSinh());
            colummData.add(listUser.get(i).getQueQuan());
            colummData.add(listUser.get(i).getLuongCung());
            colummData.add(listUser.get(i).getThuong());
            colummData.add(listUser.get(i).getPhat());
            colummData.add(listUser.get(i).thuclinh());
            colummData.add(listUser.get(i).getUsername());
            for(int j=0;j<listAccount.size();j++){
                if(listAccount.get(j).getUsername().compareTo(listUser.get(i).getUsername()) == 0){
                    colummData.add(listAccount.get(j).getPassword());
                    break;
                }
            }
            record.addRow(colummData);
        }
        jTable1.setModel(record);
    }
    public boolean validateUser(){
        if (hoten.getText().length() < 1){
            JOptionPane.showMessageDialog(rootPane, "Họ tên không được để trống!");
            return false;
        }
        if (namsinh.getText().length() < 1){
            JOptionPane.showMessageDialog(rootPane, "Năm sinh không được để trống!");
            return false;
        }
        if (quequan.getText().length() < 1){
            JOptionPane.showMessageDialog(rootPane, "Quê quán không được để trống");
            return false;
        }
        if (luongcung.getText().length() < 1){
            JOptionPane.showMessageDialog(rootPane, "Lương cứng không được để trống");
            return false;
        }
        if (thuong.getText().length() < 1){
            JOptionPane.showMessageDialog(rootPane, "Thưởng không được để trống");
            return false;
        }
        if (phat.getText().length() < 1){
            JOptionPane.showMessageDialog(rootPane, "Phạt không được để trống");
            return false;
        }
        if (username.getText().length() < 3){
            JOptionPane.showMessageDialog(rootPane, "username cần tối thiểu 3 ký tự");
            return false;
        }
        if (password.getText().length() < 6){
            JOptionPane.showMessageDialog(rootPane, "password cần tối thiểu 6 ký tự");
            return false;
        }
        User user = new User();
        try {
            user.setNamSinh(Integer.parseInt(namsinh.getText()));
            user.setLuongCung(Float.parseFloat(luongcung.getText()));
            user.setThuong(Float.parseFloat(thuong.getText()));
            user.setPhat(Float.parseFloat(phat.getText()));
            return true;
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Số liệu không hợp lệ");
            return false;
        }
    }
