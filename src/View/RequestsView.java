    public void updateTabel(){
        DefaultTableModel record = (DefaultTableModel) jTable1.getModel();
        record.setRowCount(0);
        for(int i=0;i<listRequest.size();i++){
            Vector colummData = new Vector();
            colummData.add(listRequest.get(i).getUsername());
            colummData.add(listRequest.get(i).getPassword());
            colummData.add(listRequest.get(i).getEmployeeCode());
            record.addRow(colummData);
        }
        jTable1.setModel(record);
    }
    
