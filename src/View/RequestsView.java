    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int k = jTable1.getSelectedRow();
        if(k == -1) {
            JOptionPane.showMessageDialog(rootPane, "Chọn một yêu cầu");
            return;
        }
        Request r = new Request();
        r = listRequest.get(k);
        if(luong.getText().length() < 1 || thuong.getText().length() < 1 || phat.getText().length() < 1){
            JOptionPane.showMessageDialog(rootPane, "Nhập lương thưởng và phạt để mở tài khoản");
            return;
        }
        User user = new User();
        Account acc = new Account();
        try{
            user.setHoTen("");
            user.setNamSinh(0);
            user.setQueQuan("");
            user.setLuongCung(Float.parseFloat(luong.getText()));
            user.setThuong(Float.parseFloat(thuong.getText()));
            user.setPhat(Float.parseFloat(phat.getText()));
            user.setUsername(r.getUsername());
            acc.setUsername(r.getUsername());
            acc.setPassword(r.getPassword());
            UserController.createUser(user);
            AccountController.create(acc);
            JOptionPane.showMessageDialog(rootPane, "Tạo thành công");
            AccountController.deleteRequest(r.getUsername());
            this.setVisible(false);
            RequestsView newf = new RequestsView();
            newf.setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(rootPane, "Dữ liệu không hợp lệ");
            return;
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int k = jTable1.getSelectedRow();
        if(k == -1){
            JOptionPane.showMessageDialog(rootPane, "Chọn một yêu cầu");
            return;
        }
        Request r = new Request();
        r = listRequest.get(k);
        if(JOptionPane.showConfirmDialog(rootPane, "Từ chối yêu cầu ?", "Từ chối", 0) != 0) return;
        AccountController.deleteRequest(r.getUsername());
        JOptionPane.showMessageDialog(rootPane, "Đã từ chối");
        this.setVisible(false);
        RequestsView newf = new RequestsView();
        newf.setVisible(true);
    }//GEN-LAST:event_jButton2ActionPerformed

